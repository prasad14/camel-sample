package com.sample.camel.dtos;

public class OutPutData {


	private int id;
	private String val;
	public OutPutData(int id, String val) {
		this.id=id;
		this.val=val;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getVal() {
		return val;
	}
	public void setVal(String val) {
		this.val = val;
	}
	
	

	
}
